package com.syntax.replit.hw050122;

public class ArithmeticOp2 {

	public static void main(String[] args) {
		 /* 1. Declare 2 numbers and assign values 200 and 100     
	     *    respectively.
	     * 2. First, add numbers, then subtract, third multiply,
	     *    forth divide.
	     * 3. Please use variables to print the values of each
	     *    operation result on a separate line.
	     *    Output:
	     *    300
	     *    100
	     *    20000
	     *    2
	    */
	    int x = 200, y = 100;
	    int sum, sub, mult, div;
	    sum = x + y;
	    sub = x - y;
	    mult = x * y;
	    div = x / y;
	    System.out.println(sum);
	    System.out.println(sub);
	    System.out.println(mult);
	    System.out.println(div);
	  }
	}
