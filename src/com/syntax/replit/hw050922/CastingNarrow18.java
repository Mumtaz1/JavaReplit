public class CastingNarrow18 {

	public static void main(String[] args) {
		/* # 18
		 * Write a program to convert double 500.4444 into long
	     * i.e., we will be using casting in Java and since
	     * we are converting double into long that means we
	     * will be doing narrowing (AKA explicit casting).
	    */
	    double valu = 500.444;
	    long valu1 = (long)500.4444;
	    System.out.println(valu1);
	}
}	