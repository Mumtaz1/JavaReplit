package com.syntax.replit.hw052222;

public class ForLoop54 {

	public static void main(String[] args) {
		
		/*
		 * Create a for loop that prints out numbers from 0 to 12
		 * Must not include the number 12 in the output
		 * Output:
		 * 0
		 * 1
		 * 2
		 * 3
		 * 4 all the way down to 11
		 */
		
		/*
		 * Step1: does initialization 
		 * Step 2: checks condition
		 * Step 3: does the print
		 *  Step 4: then does the increment
		 */

		for (int i = 0; i < 12; i++) {
			System.out.println(i);  
		}
	}
}