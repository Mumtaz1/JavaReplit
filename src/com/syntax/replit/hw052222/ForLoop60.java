package com.syntax.replit.hw052222;

public class ForLoop60 {

	public static void main(String[] args) {
		
		/*
		 * Step1: does initialization 
		 * Step 2: checks condition 
		 * Step 3: does the print
		 * Step 4: then does the increment
		 */
		
		/*
		 * Write a program that prints the count down from 10 to 1 
		 * and then says "Happy New Year!"
		 * Output:
		 * 10
		 * 9
		 * 8 all the way down to 1.
		 */

		for (int i = 10; i >= 1; i--) {
			if (i <= 10 || i >= 1);
			System.out.println(i);
		}	
		System.out.println("Happy New Year! " ); 
	}
}