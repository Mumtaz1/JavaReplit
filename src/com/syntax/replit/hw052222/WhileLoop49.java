package com.syntax.replit.hw052222;

public class WhileLoop49 {

	public static void main(String[] args) {
		
		/*
		 * Write a Java program to print "Hello Syntax Team! How are you?"
		 * three times using the while loop
		 */
		
		// while loop first checks condition then executes the block of code
		
		int i = 1;
		
		while (i  <= 3) {
			System.out.println("Hello Syntax Team! How are you?"); 
			i ++;
		}
	}
}