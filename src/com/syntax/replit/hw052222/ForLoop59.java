package com.syntax.replit.hw052222;

public class ForLoop59 {

	public static void main(String[] args) {
		
		/*
		 * Step1: does initialization 
		 * Step 2: checks condition 
		 * Step 3: does the print
		 * Step 4: then does the increment
		 */
		
		/* 
		 * Write a program that prints the string "Hey, it is year:" and the
		 *  years between 2011 and 2019. Must include year 2019 
		 *  in the output.
		 *  Output:
		 *  Hey, it is year:
		 *  2011
		 *  Hey, it is year:
		 *  2012
		 *  etc until 2019
		 */
		
		for (int year = 2011; year <= 2019; year++) {
			if (year >= 2011 && year <= 2019);
			System.out.println("Hey, it is year: " ); 
			System.out.println(year);
		}	
	}
}